package test.com;

public class Test02finalVariable {

	public static void main(String[] args) {
		System.out.println("final Variable");
		
		final int KOSTA_KOR = 100; // final 변수는 변수값을 바꿀 수 없음 //즉, 상수
		System.out.println(KOSTA_KOR);
//		KOR = 99;
		System.out.println(KOSTA_KOR);
		// 명시적으로 상수(final변수)는 대문자로 표기
		
		
	} // end main()

} // end class
